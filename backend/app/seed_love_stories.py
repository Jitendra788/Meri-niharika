def patch_love_story_covers(*, force: bool = False) -> int:
    """Set cover_url on love-story rows (jpg when available)."""
    ensure_love_story_cover_files()
    path = Path(settings.uploads_dir) / "articles_manifest.json"
    if not path.exists():
        return 0
    try:
        existing = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return 0
    if not isinstance(existing, list):
        return 0
    updated = 0
    for it in existing:
        if it.get("category_slug") != "love-story":
            continue
        slug = str(it.get("slug", ""))
        if not slug:
            continue
        new_url = love_story_cover(slug)
        if force or not it.get("cover_url") or str(it.get("cover_url", "")).endswith(".svg"):
            if it.get("cover_url") != new_url:
                it["cover_url"] = new_url
                updated += 1
    if updated:
        path.write_text(json.dumps(existing, ensure_ascii=False, indent=2), encoding="utf-8")
    return updated


def merge_love_stories_into_manifest() -> int:
    """Append missing love-story slugs to articles_manifest.json. Returns count added."""
    ensure_love_story_cover_files()
    path = Path(settings.uploads_dir) / "articles_manifest.json"
    existing: list[dict[str, Any]] = []
    if path.exists():
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
            existing = data if isinstance(data, list) else []
        except (json.JSONDecodeError, OSError):
            existing = []

    slugs = {str(it.get("slug", "")) for it in existing}
    added = 0
    for story in love_story_catalog():
        if story["slug"] in slugs:
            continue
        existing.append(story)
        slugs.add(story["slug"])
        added += 1

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(existing, ensure_ascii=False, indent=2), encoding="utf-8")
    patch_love_story_covers()
    return added


async def seed_love_stories_db() -> None:
    """Insert into PostgreSQL when articles table has no love-story entries."""
    from . import store

    if await store.count_articles(category_slug="love-story") > 0:
        return
    for story in love_story_catalog():
        if await store.get_article_by_slug(story["slug"]):
            continue
        await store.create_article(story)
