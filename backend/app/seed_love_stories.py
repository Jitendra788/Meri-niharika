def _now() -> datetime:
    return datetime.now(tz=timezone.utc)


def _iso(dt: datetime) -> str:
    return dt.isoformat()


def love_story_cover(slug: str) -> str:
    jpg = Path(settings.uploads_dir) / "images" / "love-story" / f"{slug}.jpg"
    if jpg.is_file():
        return f"/uploads/images/love-story/{slug}.jpg"
    return f"/uploads/images/love-story/{slug}.svg"


def ensure_love_story_cover_files() -> None:
    from pathlib import Path

    covers_dir = Path(settings.uploads_dir) / "images" / "love-story"
    expected = {f"{s}.jpg" for s in (
        "bikhari-si-raahen", "college-ki-sidhiyan", "pehli-mulaqat", "ek-aur-mauka",
        "mahak-tere-ishq-ki", "nafrat-se-mohabbat-tak", "tilism-e-mohabbat", "sanam-teri-kasam",
        "dil-se-bandhe-do-dil", "tum-jo-mil-gaye", "anubandhit-rishta", "neeta-love-story",
        "tumhari-aankhen", "muralidhara-prem-kahani", "ishqbaz-prem-kahani",
    )}
    have = {p.name for p in covers_dir.glob("*.jpg")} if covers_dir.exists() else set()
    if expected <= have:
        return
    scripts = Path(__file__).resolve().parents[1] / "scripts"
    for name in ("download_love_covers.py", "generate_love_covers.py"):
        script = scripts / name
        if not script.is_file():
            continue
        try:
            import subprocess
            import sys

            subprocess.run([sys.executable, str(script)], check=True, cwd=str(script.parent.parent))
            if expected <= {p.name for p in covers_dir.glob("*.jpg")}:
                return
        except Exception:
            continue


