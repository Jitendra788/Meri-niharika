def sync_full_love_stories() -> int:
    """Update love-story entries in manifest with full content."""
    path = Path(settings.uploads_dir) / "articles_manifest.json"
    if not path.exists():
        return 0
    items = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(items, list):
        return 0
    updated = 0
    for it in items:
        if it.get("category_slug") != "love-story":
            continue
        if it.get("series_slug"):
            continue
        slug = str(it.get("slug", ""))
        full = FULL.get(slug)
        if not full:
            continue
        it["excerpt"] = full["excerpt"]
        it["content"] = full["content"]
        tags = list(it.get("tags") or [])
        if tags and len(tags) > 1 and "भाग" in str(tags[1]):
            tags[1] = "पूर्ण कहानी"
        it["tags"] = tags
        updated += 1
    if updated:
        path.write_text(json.dumps(items, ensure_ascii=False, indent=2), encoding="utf-8")
    return updated


async def sync_full_love_stories_db() -> int:
    """Mirror full text into PostgreSQL when articles exist there."""
    from . import store

    n = 0
    for slug, full in FULL.items():
        row = await store.get_article_by_slug(slug)
        if not row:
            continue
        tags = list(row.get("tags") or [])
        if len(tags) > 1:
            tags[1] = "पूर्ण कहानी"
        await store.update_article(
            row["_id"],
            {
                "excerpt": full["excerpt"],
                "content": full["content"],
                "tags": tags,
            },
        )
        n += 1
    return n
