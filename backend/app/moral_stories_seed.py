def append_moral_stories_to_manifest() -> int:
    from .articles_store import load_articles, save_articles, unique_slug

    items = load_articles()
    existing_titles = {it.get("title") for it in items if it.get("title")}
    added = 0
    for story in moral_stories_batch():
        if story["title"] in existing_titles:
            continue
        story["slug"] = unique_slug(story["slug"], items)
        items.append(story)
        existing_titles.add(story["title"])
        added += 1
    if added:
        save_articles(items)
    return added
