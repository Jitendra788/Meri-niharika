def _slug(part: int) -> str:
    return SERIES_SLUG if part == 1 else f"{SERIES_SLUG}-bhag-{part}"


def _title(part: int) -> str:
    return f"{SERIES_TITLE} — भाग {part}"


def build_series_articles() -> list[dict[str, Any]]:
    now = datetime.now(tz=timezone.utc).isoformat()
    parts = _part_content()
    out: list[dict[str, Any]] = []
    for i, p in enumerate(parts, start=1):
        out.append(
            {
                "id": str(uuid4()),
                "title": _title(i),
                "slug": _slug(i),
                "excerpt": p["excerpt"],
                "content": p["content"].strip(),
                "cover_url": COVER if i == 1 else None,
                "category_slug": "love-story",
                "series_slug": SERIES_SLUG,
                "series_title": SERIES_TITLE,
                "part_number": i,
                "parts_total": PARTS_TOTAL,
                "tags": [AUTHOR, f"भाग {i} / {PARTS_TOTAL}", "प्रेम कहानी"],
                "language": "hi",
                "status": "published" if i == 1 else "scheduled",
                "published_at": now if i == 1 else None,
                "created_at": now,
                "updated_at": now,
            }
        )
    return out


def sync_mahak_series_to_manifest() -> int:
    path = Path(settings.uploads_dir) / "articles_manifest.json"
    items: list[dict[str, Any]] = []
    if path.exists():
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
            items = data if isinstance(data, list) else []
        except (json.JSONDecodeError, OSError):
            items = []

    # Remove old single mahak row and any prior series parts
    items = [
        it
        for it in items
        if it.get("series_slug") != SERIES_SLUG
        and not str(it.get("slug", "")).startswith(f"{SERIES_SLUG}-bhag-")
        and it.get("slug") != SERIES_SLUG
    ]

    series = build_series_articles()
    items.extend(series)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(items, ensure_ascii=False, indent=2), encoding="utf-8")
    return len(series)
